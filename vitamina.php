            var allInputs = $('.value');
            var allResults = $('.result');
            var allBase = $('.base');
            $('.value').bind('blur', function () {
                calculaDV();
            });

            function calculaDV() {
                $.each(allInputs, function (key, value) {
                    var vitaminValue = parseFloat(value.value);
                    var vitaminBase = parseFloat(allBase[key].innerText);
                    var daily = 0;
                    if (vitaminBase && vitaminValue) {
                        daily =  (vitaminValue / vitaminBase) * 100;
                    }
                    daily = daily.toFixed(2) + '%';
                    allResults[key].innerText = daily;
                    console.log(vitaminValue);

                });
            }
